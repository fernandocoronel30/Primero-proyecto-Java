
public class Wrappers {
	public static void main(String[] args) {
		
		System.out.println("****Byte****");
		System.out.println(Byte.MAX_VALUE);// 127
		System.out.println(Byte.MIN_VALUE);// -128
		System.out.println(Byte.BYTES); // 1
		System.out.println(Byte.SIZE);// 8
		
		//short, int, long, float y double
		System.out.println("****Short****");
		System.out.println(Short.MAX_VALUE);// 127
		System.out.println(Short.MIN_VALUE);// -128
		System.out.println(Short.BYTES); // 1
		System.out.println(Short.SIZE);// 8
		System.out.println(Short.TYPE);// tipo byte
		
		System.out.println("****int****");
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.BYTES);
		System.out.println(Integer.SIZE);
		
		System.out.println("****long****");
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.BYTES);
		System.out.println(Long.SIZE);
		
		System.out.println("****float****");
		System.out.println(Float.MAX_VALUE);
		System.out.println(Float.MIN_VALUE);
		System.out.println(Float.BYTES);
		System.out.println(Float.SIZE);
		
		System.out.println("****double****");
		System.out.println(Double.MAX_VALUE);
		System.out.println(Double.MIN_VALUE);
		System.out.println(Double.BYTES);
		System.out.println(Double.SIZE);
		
	}//main
}//class Wrappers
